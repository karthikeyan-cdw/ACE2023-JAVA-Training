package Q001;

// Program to print default values of primitive values
public class Default {
	static int byte_;
	static int int_;
	static float long_;
	static float float_;
	static double double_;
	static char char_;
	static boolean boolean_;
	public static void main(String[] args) {
		System.out.println("The default values of the primitive datatypes are: ");
		System.out.println("Byte: "+byte_);
		System.out.println("Int: "+int_);
		System.out.println("Long: "+long_);
		System.out.println("Float: "+float_);
		System.out.println("Double: "+double_);
		System.out.println("Char: "+char_);
		System.out.println("Boolean: "+boolean_);
	}
}
